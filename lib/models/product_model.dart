class Product {
  final int id;
  final int categoryId;
  final String name;
  final int size;
  final int sizeUnitId;
  final int typeId;

  Product({
    required this.id,
    required this.categoryId,
    required this.name,
    required this.size,
    required this.sizeUnitId,
    required this.typeId
  });
}